#Program to use break statement in list

# creating any object for example 'color'
color = ["red","yellow","blue","green","pink","orange"]
for d in color: # expressing the color in the variable 'd'
    print(d) # printing the values of d=color as stated in list
    if d == "blue": # if conditional statement: when value of list becomes "blue"
        break # program will break the computing process and print all the values predecing blue